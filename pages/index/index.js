  /** 打开/关闭表情 */
  openEmoji : function () {
    var that = this;
    this.setData({
      emojiView : !that.data.emojiView
    });
  },
  /** 表情插入 */
  emojiInput : function (e) {
    var that = this;
    var text = that.data.text;
    var textIndex = that.data.textIndex;

    var emoji = e.currentTarget.dataset.emoji;
    text = text.substring(0, textIndex) + emoji + text.substring(textIndex, text.length);

    this.setData({
      text : text,
      textIndex : textIndex + emoji.length
    });
  }
